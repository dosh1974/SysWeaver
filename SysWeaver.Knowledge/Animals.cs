        #region Setup

        static readonly Info TagAnimal = new Info("Animals", "Animals", Group, null);
        static readonly Info[] ParentTagsAnimal = [ TagAnimal ];

        static readonly IReadOnlyDictionary<String, Info> Specials = new Dictionary<String, Info>(StringComparer.Ordinal)
        {
            { "Animals", TagAnimal },
            { "Pet Animals", new Info("Pets", "Pet Animals", Group, ParentTagsAnimal) },
            { "Pet Rodents", new Info("Pet Rodents", "Pet Rodents", Group, ParentTagsAnimal) },
        }.Freeze();

        static readonly IReadOnlyDictionary<String, String> SubGroups = new Dictionary<String, String>(StringComparer.Ordinal)
        {
            { "Cats", "Mammals" },
            { "Dogs", "Mammals" },
            { "Pet Rodents", "Rodents" },
            { "Rodents", "Mammals" },
        }.Freeze();


        static readonly Dictionary<String, Info> Tags = new Dictionary<string, Info>(StringComparer.Ordinal);

        static void Reg(string name, string tags)
        {
            var t = tags.Split(',').Select(x => x.Trim()).Where(x => x.Length > 0).ToList();
            var l = t.Count;
            var subs = SubGroups;
            while (l > 0)
            {
                --l;
                if (subs.TryGetValue(t[l], out var nt))
                {
                    if (!t.Contains(nt))
                    {
                        t.Insert(l, nt);
                        ++l;
                    }
                }
            }
            l = t.Count;
            var s = Specials;
            Tuple<String, Info, bool>[] o = new Tuple<String, Info, bool>[l + 1];
            String prev = "";
            for (int i = 0; i < l; ++i)
            {
                var sub = t[i];
                var tt = sub;
                if (s.TryGetValue(tt, out var tag))
                {
                    var key = tag.Name.FastToLower();
                    o[i] = Tuple.Create(key, tag, !AllInfo.IsMultiple(key));
                    sub = null;
                }
                else
                {
                    var rparent = o.Take(i).Select(x => x.Item2).ToArray();
                    tag = new Info(tt, "Animal group " + tt + prev, Group, rparent);
                    var key = tt.FastToLower();
                    o[i] = Tuple.Create(key, tag, false);
                }
                if ((i > 0) && (sub != null))
                {
                    if (prev.Length > 0)
                        prev = " of group " + sub + " which is a member" + prev;
                    else
                        prev = " of group " + sub + prev;
                }
            }
            var fparent = o.Take(l).Select(x => x.Item2).ToArray();
            var ftag = new Info(name, "The animal " + name + prev, Group, fparent);
            var fkey = name.FastToLower();
            o[l] = Tuple.Create(fkey, ftag, true);
            Tags.TryAdd(fkey, ftag);
            ++l;
            while (l > 0)
            {
                --l;
                var v = o[l];
                AllInfo.TryAdd(v.Item1, v.Item2, false, v.Item3);
            }
        }

